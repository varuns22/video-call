package com.example.videocall;

public class Contacts {

    private String uid;

    private String name;

    public Contacts() {
    }

    public Contacts(String uid, String name) {
        this.uid = uid;
        this.name = name;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
